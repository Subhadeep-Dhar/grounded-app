// ───────────────────────────────────────────
// UTILITY: Get random message without repeating
// ───────────────────────────────────────────
let usedIndices = {};

export const getRandomMessage = (pool, category = 'default') => {
  if (!usedIndices[category]) usedIndices[category] = [];

  // Reset if all used
  if (usedIndices[category].length >= pool.length) {
    usedIndices[category] = [];
  }

  let index;
  do {
    index = Math.floor(Math.random() * pool.length);
  } while (usedIndices[category].includes(index));

  usedIndices[category].push(index);
  return pool[index];
};

export const getRandomQuote = () => {
  return getRandomMessage(MOTIVATIONAL_QUOTES, 'quotes');
};

export const getRandomTask = () => {
  return getRandomMessage(CHALLENGE_TASKS, 'tasks');
};
